FUNCTION grad2(f_in, x)
!########################################################################
IMPLICIT NONE
!########################################################################
REAL(kind=REAL64), EXTERNAL ::              f_in
REAL(kind=REAL64), INTENT(IN) ::            x
REAL(kind=REAL64) ::                        grad2
!########################################################################
REAL(kind=REAL64) ::                        h
!########################################################################
h = 1.0D-5
grad2 = (f_in(x+h) - 2.0d0 * f_in(x) + f_in(x-h)) / h**2
END FUNCTION grad2